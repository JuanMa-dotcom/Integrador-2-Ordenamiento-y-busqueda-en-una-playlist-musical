private void actualizarTabla(int filaResaltada) {
    modeloTabla.setRowCount(0); // limpiar filas
    Cancion[] canciones = manager.getPlaylist();
    for (int i = 0; i < canciones.length; i++) {
        modeloTabla.addRow(new Object[]{
            i + 1,
            canciones[i].nombre,
            canciones[i].artista,
            canciones[i].album
        });
    }
 
    // Resaltar fila encontrada en búsqueda
    if (filaResaltada >= 0) {
        jTable1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable t, Object v,
                boolean sel, boolean foc, int row, int col) {
                super.getTableCellRendererComponent(t, v, sel, foc, row, col);
                if (row == filaResaltada) {
                    setBackground(new Color(5, 150, 105));
                    setForeground(Color.WHITE);
                } else {
                    setBackground(row % 2 == 0 ? new Color(30, 30, 40) : new Color(22, 22, 32));
                    setForeground(new Color(232, 230, 223));
                }
                return this;
            }
        });
        jTable1.repaint();
        jTable1.scrollRectToVisible(jTable1.getCellRect(filaResaltada, 0, true));
    } else {
        jTable1.setDefaultRenderer(Object.class, new DefaultTableCellRenderer() {
            @Override
            public Component getTableCellRendererComponent(JTable t, Object v,
                boolean sel, boolean foc, int row, int col) {
                super.getTableCellRendererComponent(t, v, sel, foc, row, col);
                setBackground(row % 2 == 0 ? new Color(30, 30, 40) : new Color(22, 22, 32));
                setForeground(new Color(232, 230, 223));
                return this;
            }
        });
        jTable1.repaint();
    }
}
 
