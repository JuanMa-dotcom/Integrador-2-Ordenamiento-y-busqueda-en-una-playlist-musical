//Este es un resumen del arreglo para mostrar antes/despues
private String obtenerEstadoResumido() {
    Cancion[] canciones = manager.getPlaylist();
    StringBuilder sb = new StringBuilder();
    int mostrar = Math.min(canciones.length, 5);
    for (int i = 0; i < mostrar; i++) {
        sb.append("  ").append(i + 1).append(". ").append(canciones[i].nombre)
          .append(" — ").append(canciones[i].artista).append("\n");
    }
    if (canciones.length > 5) sb.append("  ... y ").append(canciones.length - 5).append(" más.");
    return sb.toString();

    }//GEN-LAST:event_btnSalirActionPerformed

