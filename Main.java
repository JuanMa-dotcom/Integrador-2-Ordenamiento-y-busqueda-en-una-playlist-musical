    private void btnOrdenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrdenarActionPerformed
     if (manager.estaVacia()) {
        setStatus("La playlist está vacía.", Color.YELLOW);
        return;
    }
 
    // Mostrar estado ANTES
    String[] opciones = {"Por Nombre", "Por Artista"};
    int eleccion = JOptionPane.showOptionDialog(this,
        "¿Por que campo ordenar?\n\nEstado actual (antes del ordenamiento):\n" + obtenerEstadoResumido(),
        "QuickSort — Elegir criterio",
        JOptionPane.DEFAULT_OPTION,
        JOptionPane.QUESTION_MESSAGE,
        null, opciones, opciones[0]);
 
    if (eleccion == JOptionPane.CLOSED_OPTION) return;
 
    criterioOrden = (eleccion == 1)
        ? PlaylistManager.ORDENAR_POR_ARTISTA
        : PlaylistManager.ORDENAR_POR_NOMBRE;
 
    // Ejecutar QuickSort (lo validamos con un true)
    manager.quickSort(criterioOrden);
    estaOrdenado = true;
 
    // Mostrar estado DESPUÉS
    actualizarTabla(-1);
    String campo = (criterioOrden == PlaylistManager.ORDENAR_POR_ARTISTA) ? "Artista" : "Nombre";
    setStatus("Playlist ordenada por " + campo + " con QuickSort.", new Color(125, 211, 168));
 
    JOptionPane.showMessageDialog(this,
        "Ordenamiento completado.\nCriterio: " + campo +
        "\nEstado despues del ordenamiento:\n" + obtenerEstadoResumido(),
        "QuickSort finalizado", JOptionPane.INFORMATION_MESSAGE);   
    }//GEN-LAST:event_btnOrdenarActionPerformed

    //Metodo para buscar una cancion en la playlist usando búsqueda binaria
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
     if (manager.estaVacia()) {
        setStatus("La playlist está vacia.", Color.YELLOW);
        return;
    }
    if (!estaOrdenado) {
        int resp = JOptionPane.showConfirmDialog(this,
            "El arreglo debe estar ordenado para la búsqueda binaria.\n¿Ordenar ahora por Nombre?",
            "Ordenar primero", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
            manager.quickSort(PlaylistManager.ORDENAR_POR_NOMBRE);
            criterioOrden = PlaylistManager.ORDENAR_POR_NOMBRE;
            estaOrdenado = true;
            actualizarTabla(-1);
        } else return;
    }
 
    String[] opcionesBusqueda = {"Por Nombre", "Por Artista"};
    int tipoBusqueda = JOptionPane.showOptionDialog(this,
        "¿Buscar por?", "Tipo de busqueda",
        JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE,
        null, opcionesBusqueda, opcionesBusqueda[0]);
 
    if (tipoBusqueda == JOptionPane.CLOSED_OPTION) return;
 
    String termino = JOptionPane.showInputDialog(this,
        "Ingresa el " + opcionesBusqueda[tipoBusqueda] + " a buscar:");
    if (termino == null || termino.trim().isEmpty()) return;
 
    int indice = (tipoBusqueda == 1)
        ? manager.buscarPorArtista(termino.trim())
        : manager.buscarPorNombre(termino.trim());
 
    if (indice == -1) {
        actualizarTabla(-1);
        setStatus("No se encontro: \"" + termino + "\"", Color.RED);
        JOptionPane.showMessageDialog(this,
            "No se encontro ninguna cancion con: \"" + termino + "\"",
            "Busqueda", JOptionPane.WARNING_MESSAGE);
    } else {
        Cancion encontrada = manager.getCancion(indice);
        actualizarTabla(indice);
        setStatus("Encontrada en posición " + (indice + 1) + ": " + encontrada.nombre, new Color(125, 211, 168));
        JOptionPane.showMessageDialog(this,
            "Cancion encontrada!\n\n" +
            "Posicion : " + (indice + 1) + " de " + manager.getTamanio() + "\n" +
            "Nombre   : " + encontrada.nombre + "\n" +
            "Artista  : " + encontrada.artista + "\n" +
            "Album    : " + encontrada.album,
            "Resultado de busqueda", JOptionPane.INFORMATION_MESSAGE);
    }
   
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
       if (manager.estaLleno()) {
        setStatus("⚠ La playlist está llena (máx. 50 canciones).", Color.YELLOW);
        return;
    }
 
    JTextField tfNombre  = new JTextField(20);
    JTextField tfArtista = new JTextField(20);
    JTextField tfAlbum   = new JTextField(20);
 
    JPanel panel = new JPanel();
    panel.setLayout(new BoxLayout(panel, BoxLayout.Y_AXIS));
    panel.add(new JLabel("Nombre de la canción:"));  panel.add(tfNombre);
    panel.add(Box.createVerticalStrut(6));
    panel.add(new JLabel("Artista:"));               panel.add(tfArtista);
    panel.add(Box.createVerticalStrut(6));
    panel.add(new JLabel("Álbum (opcional):"));      panel.add(tfAlbum);
 
    int res = JOptionPane.showConfirmDialog(this, panel, "Agregar canción", JOptionPane.OK_CANCEL_OPTION);
    if (res != JOptionPane.OK_OPTION) return;
 
    String nombre  = tfNombre.getText().trim();
    String artista = tfArtista.getText().trim();
    if (nombre.isEmpty() || artista.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nombre y Artista son obligatorios.");
        return;
    }
 
    manager.agregar(nombre, artista, tfAlbum.getText().trim());
    estaOrdenado = false;
    actualizarTabla(-1);
    setStatus("✔ \"" + nombre + "\" agregada. Total: " + manager.getTamanio(), new Color(125, 211, 168));

    }//GEN-LAST:event_btnAgregarActionPerformed

